/**@brief Function for handling the data from the OSSW.
 *
 * @param[in] p_ossw   OSSW Service structure.
 * @param[in] p_data   Data to be send to UART module.
 * @param[in] length   Length of the data.
 */
/**@snippet [Handling the data received over BLE] */
static void ossw_data_handler(ble_ossw_t * p_ossw, uint8_t * p_data, uint16_t length)
{ 
		#ifdef OSSW_DEBUG
				sd_nvic_critical_region_enter(0);
				printf("DAT: 0x%02x 0x%02x 0x%02x\r\n", p_data[0], p_data[1], p_data[2]);
				sd_nvic_critical_region_exit(0);
		#endif
	
	 switch(p_data[0]) {	
		 case 0x01:
					scr_mngr_handle_event(SCR_EVENT_APP_CONNECTION_CONFIRMED, 0);
					break;
		 case 0x10:
			    // set current time
					rtc_set_current_time(((uint32_t)p_data[1]<<24) | ((uint32_t)p_data[2]<<16)
						| ((uint32_t)p_data[3]<<8) | (uint32_t)p_data[4]);
					break; 
		 case 0x40:
					// command first part
					command_reset_data();
					command_append_data(p_data+1, length-1);
					break;
		 case 0x41:
					// command next part
					command_append_data(p_data+1, length-1);
					break;
		 case 0x42:
					// command last part
					command_append_data(p_data+1, length-1);
					command_data_complete();
					break;
		 case 0x43:
					// command first and last part
					command_reset_data();
					command_append_data(p_data+1, length-1);
					command_data_complete();
					break;
	 }
}

void ble_peripheral_confirm_command_processed(uint8_t respCode) {
	  uint8_t data[] = {0x40, respCode};
	  ble_ossw_string_send(&m_ossw, data, sizeof(data));
}

void ble_peripheral_invoke_external_function(uint8_t function_id) {
	  uint8_t data[] = {0x11, function_id};
	  ble_ossw_string_send(&m_ossw, data, sizeof(data));
}

void ble_peripheral_invoke_notification_function(uint8_t function_id) {
	  uint8_t data[] = {0x12, function_id};
	  ble_ossw_string_send(&m_ossw, data, sizeof(data));
}

void ble_peripheral_invoke_notification_function_with_data(uint8_t function_id, uint8_t* additional_data, uint8_t data_size) {
	  uint8_t data[data_size+2];
		data[0] = 0x12;
		data[1] = function_id;
		memcpy(data+2, additional_data, data_size);
	  ble_ossw_string_send(&m_ossw, data, data_size+2);
}

void ble_peripheral_set_watch_set_id(uint32_t watch_set_id) {
	  uint8_t data[] = {0x10, (watch_set_id>>24)&0xFF, (watch_set_id>>16)&0xFF, (watch_set_id>>8)&0xFF, watch_set_id&0xFF};
	  ble_ossw_string_send(&m_ossw, data, sizeof(data));
}

/**@brief Function for performing battery measurement and updating the Battery Level characteristic
 *        in Battery Service.
 */
void battery_level_update(void)
{
    uint32_t err_code;
    uint8_t  battery_level;

    battery_level = (uint8_t)battery_get_level();

    err_code = ble_bas_battery_level_update(&m_bas, battery_level);
    if ((err_code != NRF_SUCCESS) &&
        (err_code != NRF_ERROR_INVALID_STATE) &&
        (err_code != BLE_ERROR_NO_TX_BUFFERS) &&
        (err_code != BLE_ERROR_GATTS_SYS_ATTR_MISSING)
        )
    {
        APP_ERROR_HANDLER(err_code);
    }
}

/**@brief Function for the GAP initialization.
 *
 * @details This function sets up all the necessary GAP (Generic Access Profile) parameters of the
 *          device including the device name, appearance, and the preferred connection parameters.
 */
static void gap_params_init(void)
{
    uint32_t                err_code;
    ble_gap_conn_params_t   gap_conn_params;
    ble_gap_conn_sec_mode_t sec_mode;

    BLE_GAP_CONN_SEC_MODE_SET_OPEN(&sec_mode);

    err_code = sd_ble_gap_device_name_set(&sec_mode,
                                          (const uint8_t *)DEVICE_NAME,
                                          strlen(DEVICE_NAME));
    APP_ERROR_CHECK(err_code);

    err_code = sd_ble_gap_appearance_set(BLE_APPEARANCE_WATCH_SPORTS_WATCH);
    APP_ERROR_CHECK(err_code);

    memset(&gap_conn_params, 0, sizeof(gap_conn_params));

    gap_conn_params.min_conn_interval = MIN_CONN_INTERVAL;
    gap_conn_params.max_conn_interval = MAX_CONN_INTERVAL;
    gap_conn_params.slave_latency     = SLAVE_LATENCY;
    gap_conn_params.conn_sup_timeout  = CONN_SUP_TIMEOUT;

    err_code = sd_ble_gap_ppcp_set(&gap_conn_params);
    APP_ERROR_CHECK(err_code);
}


